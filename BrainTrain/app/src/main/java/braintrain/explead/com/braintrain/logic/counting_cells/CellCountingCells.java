public class CellCountingCells {

    private int x;
    private int y;
    private int id;

    public CellCountingCells(int x, int y, int id) {
        this.x = x;
        this.y = y;
        this.id = id;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getId() {
        return id;
    }
}
